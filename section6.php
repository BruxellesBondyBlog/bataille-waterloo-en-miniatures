<h1>16 h 30</h1>

<article class="content">
	<div>				
		<p>Les Prussiens lancent enfin l’attaque. Mais un moment plus décisif se passe ailleurs. Depuis 16 heures, les canons français, dirigés par le Maréchal Ney, tirent sur les premières lignes des troupes de Wellington. Ce dernier leur ordonne de reculer de 100 mètres pour échapper aux boulets.
		</p>

<img src="images/12.jpg" alt="Troupes alliées" width="100%" />
				
		<p>A cause de la topographie des lieux, les Français ne voient plus les Alliés et croient qu’ils ont fui. Ney ordonne alors la charge des cuirassiers entre les fermes de la Haie Sainte et d’Hougoumont. Ceux-ci sont suivis par toute la cavalerie de réserve de la garde impériale, sans qu’aucun ordre ne leur soit pourtant donné. Ce qui n’est pas normal. 10.000 cavaliers foncent ainsi dans la plaine entre La Haie Sainte et Hougoumont, soit à peine 900 mètres de distance. En deux heures, ils lanceront une douzaine d’assauts. La cavalerie française finira à bout de force.
		</p>
