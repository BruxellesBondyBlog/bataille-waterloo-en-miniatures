		<p>L’attaque principale commence. Quatre divisions napoléoniennes en colonnes se lance en direction de la ligne des Alliés. Entre 4.000 et 5.000 hommes composent chaque section. Les Français sont repoussés par la cavalerie néerlandaise. Cette dernière poursuit sa chevauchée meurtrière et attaque la grande batterie napoléonienne.
