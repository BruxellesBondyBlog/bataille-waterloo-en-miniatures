<h1 style="margin-top:-4%;">Pendant ce temps-là <br />chez les Prussiens (partie 2)</h1>

<article class="content">
	<div>				
		<p>Par Genval, les forces armées de von Ziethen (<sup>1er</sup> corps prussien ), divisées en trois parties, se dirigent vers le champ de bataille.
		</p>
<img src="images/16.jpg" alt="Forces prussiennes à Genval" width="100%" />
			
		<p>Vers 18h15, elles sont près de Chapelle Notre Dame de Bon-Secours. Ordre leur est donné par Blücher d’aller repousser les troupes françaises localisées à Plancenoit au lieu d’aider l’armée de Wellington.
		</p>

<img src="images/17.jpg" alt="Le prince Guillaume et le Général Blücher" width="100%" />
				
		<p>Le Lieutenant-Colonel von Reiche, chef d’état-major du  général prussien Von Zieten refuse à deux reprises. La troisième fois, sous la menace d’un jugement par le tribunal militaire, le général von Ziethen accepte. Mais précise qu’il n’a pas beaucoup de troupes sous la main. Ce qui est faux. C’est ainsi que le 3<sup>e</sup> bataillon du 24<sup>e</sup> de ligne  suit l’aide de camp de Blücher alors que les autres troupes se dirigent tout droit vers les troupes anglaises pour les rejoindre. 
		</p>

