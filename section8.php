<h1 style="margin-top:-4%;">Pendant ce temps-là, <br/>du côté des Prussiens (partie 1)</h1>

<article class="content">
	<div>				
		<p>Les forces prussiennes passent le bois de Paris et attaque les troupes du comte de Lobau. Plus à l’ouest, le village de Plancenoit est attaqué par les Prussiens de Bülow.
		</p>
	
		<img src="images/14.jpg" alt="Village de Plancenoit" width="100%" />
    			
		<p>Napoléon envoie des renforts de la jeune garde pour contenir cette offensive. Vers 18 heures, les Prussiens reçoivent eux aussi de l’aide. La jeune garde ne peut plus tenir. Il faut le soutien de deux bataillons de la vieille garde, l’élite des troupes napoléoniennes, pour repousser les Prussiens. Beaucoup de sang est versé. « Ça a fumé par là », ironise Willy Smout. La garde finit par refouler les Prussiens hors de Plancenoit.
		</p>
