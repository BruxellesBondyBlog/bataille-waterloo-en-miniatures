<h1 style="padding-top:70px;text-align:center;">Et le Lion de Waterloo dans tout ça ?</h1>

<article class="content">
	<div>				
		<p>Le Lion de Waterloo est située sur le point culminant du champ de bataille, zone occupée en 1815 par les Anglais.
		</p>
	</div>
</article>
<img src="images/25.jpg" alt="Butte du Lion" width="100%" />

<article class="content">
	<div>				
		<p>C’est en 1823 que le roi Guillaume I<sup>er</sup> des Pays-Bas prend la décision de créer ce monument car son fils, le prince Frédéric d’Orange-Nassau, y a été blessé à l’épaule à la fin de la bataille. Le projet sera finalisé en 1826. Le terrain menant vers la chaussée sera ensuite aplani, ce qui fait dire à Wellington en 1828 : <em>« They have ruined my battlefield ! »</em> (<em>"Ils ont ruiné mon champ de bataille !"</em>)
		</p>
	</div>
</article>

