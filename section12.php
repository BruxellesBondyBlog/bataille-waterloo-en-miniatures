<h1 style="margin-top:-4%;">19 h 30</h1>

<article class="content">
	<div>				
		<p>Le Prussien Von Zieten rappelle ses troupes pour attaquer le village de Plancenoit par Fichermont. Les canons prussiens tirent par erreur sur les Nassauviens (2<sup>e</sup> division néerlandaise) dont l’uniforme est de type napoléonien avec un « N » sur leur shako. Au bout de dix minutes de méprise, les Alliés se reconnaissent et repoussent les troupes françaises du général Durutte.
		</p>
        
<img src="images/22.jpg" alt="Troupes prussiennes prêts à charger" width="100%" />
			
		<p>Pendant ce temps, Napoléon voit que des canons attaquent les Nassauviens. Il pense alors que c’est le maréchal Grouchy. Nouvelle erreur d’interprétation lourde de conséquences. Un événement unique va se dérouler. Les simples soldats du général Drouet d’Erlon se sentent trahis par les officiers car les aides de camp leur ont annoncé que les troupes de Grouchy venaient les aider. Ce qui n’est pas le cas. Les soldats décident alors de déserter. C’est le sauve-qui-peut général sur le flanc droit des Français.
		</p>
	
<img src="images/23.jpg" alt="Première ligne anglaise prête pour l'assaut final" width="100%" />
		
		<p>Wellington, de son côté, voit que Napoléon est à bout avec sa garde et n’apprécie que moyennement l’avancée tonitruante des Prussiens. En effet, c’est lui, Duc de Wellington, qui doit sortir grand vainqueur de cette bataille de Waterloo. Il indique alors à toute sa ligne d’avancer.  C’est l’assaut final !
		</p>
