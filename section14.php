		<p>Les Alliés ont remporté la bataille de Waterloo (pour les Anglais), aussi appelée à l’époque bataille de la Belle-Alliance par les Prussiens ou bataille du Mont Saint-Jean par les Français. Le bilan humain est très lourd. Près de 26.000 soldats tués ou blessés sont recensés dans le camp français, 17.000 chez les Anglais et environ 7.000 chez les Prussiens. Si l’on tient compte des disparus et des prisonniers, les Français ont perdu au total plus de 40.000 hommes à Waterloo.
