<h1 style="margin-top:-4%;">Napoléon, bien trop confiant</h1>

<article class="content">
	<div>				
		<p>Napoléon fait une nouvelle erreur. Il envoie un bulletin signifiant que « la bataille du Mont Saint-Jean est gagnée ». Or, il se trompe. Il vient de confondre la ferme de Mont Saint-Jean avec celle de la Haie Sainte. Mais pourquoi une telle méprise ? L’Empereur dispose uniquement de vieilles cartes. Celles-ci indiquent que la chaussée de Waterloo passe d’un côtéde la ferme de Mont Saint-Jean. Or, il y a eu du changement depuis. La route passe désormais de l’autre côté.
		</p>
        <img src="images/15.jpg" alt="Napoléon lors de la bataille de Waterloo" width="100%" />			
		<p>Depuis le début de la bataille, Napoléon fait preuve d’excès de confiance. <em>« Je vais prouver que Wellington est un mauvais général. C’est l’affaire d’un petit-déjeûner, Messieurs »</em>, se plait-il à ses généraux qui avaient déjà dû s’incliner face au stratège anglais…
		</p>
