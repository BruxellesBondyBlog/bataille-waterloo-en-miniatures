<h1>13 heures</h1>

<article class="content">
	<div>				
		<p>Les hostilités commencent mais ce n’est pas encore le moment des affrontements décisifs. La grande batterie de Napoléon, comprenant 80 canons, fait pleuvoir les boulets sur les forces alliées.
		</p>
		<img src="images/4.jpg" alt="Cavaliers français" width="100%" />			
		<p>Le plus jeune frère de l’Empereur, Jérôme Bonaparte, lance l’attaque sur le château-ferme d’Hougoumont. <em>« Il y est allé bataillon par bataillon. Il n’avait pas pris d’artillerie, juste l’infanterie</em>, indique Willy Smout. <em>On ne reverra aucun soldat, à part un jeune tambour qui racontera la bataille de la ferme par la suite.</em> » Au total, un corps d’armée, soit près de 16.500 hommes, vont lutter toute la journée contre 7.000 Anglais et Nassauviens. En vain. Ces derniers sont trop bien barricadés.
		</p>
