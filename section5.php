<h1 style="margin-top: -4%;">Entre-temps</h1>

<article class="content">
	<div>				
		<p>Napoléon constate qu’au loin, des baïonnettes scintillent au soleil. Celles-ci sont sur la position de Lasne-Chapelle-Saint-Lambert. Toutefois, impossible pour l’Empereur de savoir dans quel camp sont ces troupes.
		</p>
		<img src="images/8.jpg" alt="Les troupes prussiennes arrivent au loin" width="100%" />			
		<p>Napoléon pense que c'est seulement les 30.000 soldats de von Bülow. Erreur ! Ce sont les 52.000 Prussiennes de Blücher.</p>
		<img src="images/9.jpg" alt="Troupes françaises de Napoléon à Waterloo" width="100%" />			
		<p>Le comte de Lobau est envoyé par Napoléon avec 10.000 hommes pour bloquer ces troupes, en attendant l’arrivée de Grouchy, censé débarqué à revers des Prussiens. Ces derniers peinent à avancer, le profil accidenté du terrain les ralentit. Des arbres sont même nécessaires pour stopper la course des canons dans les rues jouxtant la chapelle Saint-Lambert.
