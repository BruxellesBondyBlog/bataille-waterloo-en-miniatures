<h1>18 heures</h1>

<article class="content">
	<div>				
		<p>Pendant que le maréchal Ney dirige l’attaque, Napoléon est absent du champ de bataille. Ce n’est qu’à 18 heures qu’il réapparaîtra. Son objectif : prendre la ferme de la Haie Sainte avec les troupes d’infanterie et celles du génie. A 18 h 15, l’opération est réussie.
		</p>
