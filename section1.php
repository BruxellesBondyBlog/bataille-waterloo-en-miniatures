		<p>Napoléon arrive sur le champ de bataille de Waterloo avec l’esprit de conquête. Face à lui, sur la position reconnue du Mont Saint-Jean, 69.000 Anglais dirigés par le Duc de Wellington sont prêts à en découdre. 52.000 soldats prussiens, battus mais pas définitivement vaincus par les Français le 16 juin 1815 à Ligny, sont aussi présents aux abords du champ de bataille. Afin de les prendre à revers, Napoléon charge Grouchy de les poursuivre.
